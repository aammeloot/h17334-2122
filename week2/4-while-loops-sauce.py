# Program for a pasta sauce, using while loops

print('Pan on low heat')

# Python for loop statement
# It repeats for as long as the variable tomato
# Contains 'yes'

tomato = 'yes'

while tomato == 'yes':
    print('Chopping next available tomato')
    print('Adding chopped tomato to pan')
    tomato = input('Do you want to chop another one? (yes/no)')

# After the loop
print('Add spices')
print('Cook for 20 minutes')

