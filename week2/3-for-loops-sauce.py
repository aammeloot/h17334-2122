# Program for a pasta sauce, using for loops

print('Pan on low heat')

# Python for loop statement
# It repeats as many times as a collection of data
# (list etc)
# Special data type called range allows for counters

for tomato in range(4):
    print('Chopping tomato number ', tomato + 1)
    print('Putting chopped tomato in the pan')

# After the loop
print('Add spices')
print('Cook for 20 minutes')

